package com.gee.assigningoperator;

public class AssigningoperatorDemo1 {
    public static void main(String[] args) {
        //+=
        //规则：将左边和右边进行相加，然后把结果赋给左边；
        int a = 10;
        int b = 20;
        a += b;
//       等于a=a+b;

        System.out.println(a);
        System.out.println(b);
    }
}
